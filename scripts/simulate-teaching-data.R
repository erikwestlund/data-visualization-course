# Simulate swappable cousin datasets for course teaching data.
#
# Files are written to data/simulated/ with the same basenames and column names
# as selected real files in data/real/. This makes it easy to swap real and
# simulated examples by changing only the directory in a path.

required_packages <- c("dplyr", "readr", "stringr", "tidyr")
missing_packages <- required_packages[!vapply(required_packages, requireNamespace, logical(1), quietly = TRUE)]

if (length(missing_packages) > 0) {
  stop(
    "Install required packages before running this script: ",
    paste(missing_packages, collapse = ", "),
    call. = FALSE
  )
}

library(dplyr)
library(readr)
library(stringr)
library(tidyr)

set.seed(20260604)

output_dir <- "data/simulated"
codebook_dir <- "data/codebooks"

dir.create(output_dir, recursive = TRUE, showWarnings = FALSE)
dir.create(codebook_dir, recursive = TRUE, showWarnings = FALSE)

write_sim_csv <- function(data, filename) {
  path <- file.path(output_dir, filename)
  write_csv(data, path, na = "")
  message("Wrote ", path, " (", nrow(data), " rows, ", ncol(data), " columns)")
}

write_sim_codebook <- function(filename, real_file, topic, unit, notes) {
  path <- file.path(codebook_dir, filename)
  writeLines(
    c(
      paste0("# Simulated ", str_remove(filename, "\\.md$")),
      "",
      "Source: simulated teaching data generated by `scripts/simulate-teaching-data.R`.",
      "",
      paste0("Designed as a swappable cousin for `", real_file, "`."),
      "",
      paste0("Topic: ", topic),
      "",
      paste0("Unit of observation: ", unit),
      "",
      "Important caution: these data are fully simulated. They should be used for teaching, practice, and demonstrations only.",
      "",
      "Notes:",
      "",
      paste0("- ", notes)
    ),
    path
  )
  message("Wrote ", path)
}

sample_cat <- function(values, n, prob = NULL) {
  sample(values, n, replace = TRUE, prob = prob)
}

bounded_normal <- function(n, mean, sd, lower = -Inf, upper = Inf) {
  pmin(pmax(rnorm(n, mean, sd), lower), upper)
}

missing_some <- function(x, prop) {
  idx <- sample(seq_along(x), size = floor(length(x) * prop))
  x[idx] <- NA
  x
}

logit <- function(x) 1 / (1 + exp(-x))

