simulate_food_day <- function(people, day) {
  active <- people |>
    filter(day == 1 | two_days)
  records_per_day <- sample(5:22, nrow(active), replace = TRUE)
  expanded <- active[rep(seq_len(nrow(active)), records_per_day), ] |>
    group_by(respondent_id) |>
    mutate(food_line = row_number()) |>
    ungroup()
  n <- nrow(expanded)
  chosen <- sample(seq_len(nrow(food_profile)), n, replace = TRUE, prob = c(0.18, 0.10, 0.08, 0.10, 0.12, 0.08, 0.08, 0.07, 0.05, 0.08, 0.03, 0.03))
  grams <- rgamma(n, shape = 3, scale = food_profile$base_grams[chosen] / 3)
  kcal <- pmax(0, grams * food_profile$kcal_per_gram[chosen] + rnorm(n, 0, 8))
  source_code <- food_profile$source[chosen]
  tibble(
    respondent_id = expanded$respondent_id,
    dietary_day = day,
    dietary_day1_weight = expanded$dietary_day1_weight,
    dietary_2day_weight = expanded$dietary_2day_weight,
    food_line = expanded$food_line,
    recall_status = "reliable",
    breastfed_infant_either_day = if_else(expanded$age_years < 1 & runif(n) < 0.25, "yes", "no"),
    intake_days_available = if_else(expanded$two_days, 2, 1),
    days_between_intake_and_household_interview = sample(-20:120, n, replace = TRUE),
    intake_weekday = sample_cat(c("Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"), n),
    interview_language = sample_cat(c("English", "Spanish", "English and Spanish", "Other"), n, c(0.88, 0.10, 0.015, 0.005)),
    combination_food_number = sample(0:8, n, replace = TRUE, prob = c(0.60, rep(0.05, 8))),
    combination_food_type = sample_cat(c("Not in combination", "Beverage with additions", "Cereal with additions", "Salad", "Sandwiches", "Other mixtures"), n, c(0.60, 0.10, 0.06, 0.08, 0.09, 0.07)),
    eating_time = sprintf("%02d:%02d:00", sample(6:23, n, replace = TRUE), sample(c(0, 15, 30, 45), n, replace = TRUE)),
    eating_occasion = sample_cat(c("Breakfast", "Lunch", "Dinner", "Supper", "Snack", "Beverage/Drink"), n, c(0.20, 0.23, 0.25, 0.05, 0.20, 0.07)),
    food_source_code = source_code,
    food_source = food_source_label(source_code),
    food_source_group = food_source_group(source_code),
    eaten_at_home = sample_cat(c("yes", "no"), n, c(0.68, 0.32)),
    usda_food_code = food_profile$usda_food_code[chosen],
    grams = round(grams, 2),
    energy_kcal = round(kcal),
    protein_g = round(pmax(0, kcal * runif(n, 0.025, 0.07) / 4), 2),
    carbohydrate_g = round(pmax(0, kcal * runif(n, 0.35, 0.62) / 4), 2),
    total_sugars_g = round(pmax(0, kcal * runif(n, 0.03, 0.22) / 4), 2),
    dietary_fiber_g = round(pmax(0, kcal * runif(n, 0.003, 0.025) / 2), 2),
    total_fat_g = round(pmax(0, kcal * runif(n, 0.18, 0.45) / 9), 2),
    saturated_fat_g = round(pmax(0, kcal * runif(n, 0.04, 0.16) / 9), 3),
    cholesterol_mg = round(pmax(0, rnorm(n, kcal * 0.05, 12))),
    sodium_mg = round(pmax(0, kcal * runif(n, 0.55, 1.7))),
    potassium_mg = round(pmax(0, kcal * runif(n, 0.3, 1.6))),
    caffeine_mg = round(if_else(usda_food_code %in% c(92101000), runif(n, 20, 160), runif(n, 0, 5))),
    alcohol_g = round(if_else(expanded$age_years >= 21 & runif(n) < 0.06, runif(n, 3, 28), 0), 1)
  )
}

