wellness_measures <- tibble(
  measure_key = c("current_smoking", "diabetes", "frequent_mental_distress", "high_blood_pressure", "lack_health_insurance", "obesity", "physical_inactivity"),
  measure_id = c("CSMOKING", "DIABETES", "MHLTH", "BPHIGH", "ACCESS2", "OBESITY", "LPA"),
  measure = c("Current smoking", "Diabetes", "Frequent mental distress", "High blood pressure", "Lack of health insurance", "Obesity", "Physical inactivity"),
  base = c(14, 10, 15, 31, 11, 33, 25)
)

n_counties <- 520
county_base <- tibble(
  year = 2023,
  state = sample(state.abb, n_counties, replace = TRUE),
  county_id = seq_len(n_counties),
  tract_count = sample(1:80, n_counties, replace = TRUE),
  total_population = round(rlnorm(n_counties, log(65000), 1.0)),
  total_adult_population = round(total_population * runif(n_counties, 0.68, 0.84)),
  vulnerability = rnorm(n_counties)
) |>
  mutate(
    state_name = state.name[match(state, state.abb)],
    county_fips = sprintf("%02d%03d", match(state, state.abb), county_id %% 999),
    county_name = paste("County", str_pad(county_id, 3, pad = "0"))
  )

places_county_core_measures_long <- tidyr::crossing(county_base, wellness_measures) |>
  mutate(
    estimate = pmax(1, pmin(60, base + 4 * vulnerability + rnorm(n(), 0, 3))),
    ci_width = runif(n(), 1.5, 5.5) + 35000 / pmax(total_adult_population, 2000),
    ci_lower = pmax(0, estimate - ci_width),
    ci_upper = pmin(100, estimate + ci_width),
    across(c(estimate, ci_lower, ci_upper), ~ round(.x, 1))
  ) |>
  select(year, state, state_name, county_fips, county_name, tract_count, total_population, total_adult_population, measure_id, measure_key, measure, estimate, ci_lower, ci_upper) |>
  arrange(state, county_name, measure_key)

