# Davis-style measured/reported height and weight ------------------------------

n_davis <- 220
davis_sex <- sample_cat(c("female", "male"), n_davis, c(0.55, 0.45))
davis_height <- if_else(
  davis_sex == "female",
  bounded_normal(n_davis, 164, 7, 145, 188),
  bounded_normal(n_davis, 177, 8, 155, 205)
)
davis_bmi <- if_else(
  davis_sex == "female",
  bounded_normal(n_davis, 24.5, 4.8, 16, 46),
  bounded_normal(n_davis, 26.0, 5.2, 17, 48)
)
davis_weight <- davis_bmi * (davis_height / 100)^2
davis_reported_weight <- davis_weight + if_else(davis_sex == "female", rnorm(n_davis, -1.8, 3.2), rnorm(n_davis, 0.8, 3.8))
davis_reported_height <- davis_height + if_else(davis_sex == "female", rnorm(n_davis, -1.5, 2.2), rnorm(n_davis, -2.0, 2.8))
davis_reported_weight <- missing_some(round(davis_reported_weight), 0.08)
davis_reported_height <- missing_some(round(davis_reported_height), 0.08)
davis_weight[12] <- 158
davis_height[12] <- 58

davis_height_weight <- tibble(
  participant_id = seq_len(n_davis),
  sex = davis_sex,
  weight_kg = round(davis_weight),
  height_cm = round(davis_height),
  reported_weight_kg = davis_reported_weight,
  reported_height_cm = davis_reported_height
) |>
  mutate(
    measured_bmi = weight_kg / (height_cm / 100)^2,
    reported_bmi = reported_weight_kg / (reported_height_cm / 100)^2,
    weight_reporting_error_kg = reported_weight_kg - weight_kg,
    height_reporting_error_cm = reported_height_cm - height_cm,
    missing_reported_weight = is.na(reported_weight_kg),
    missing_reported_height = is.na(reported_height_cm),
    likely_swapped_measured_values = weight_kg > 120 & height_cm < 100
  )

write_sim_csv(davis_height_weight, "measured_vs_self_reported_exercise_minutes.csv")
write_sim_codebook("simulated_measured_vs_self_reported_exercise_minutes.md", "data/real/davis_height_weight.csv", "measured and self-reported exercise minutes", "one person", "Uses the same columns as the height/weight dataset, but frames the records as a measurement-error exercise-minute study.")

# Migraine-style headache diary ------------------------------------------------

n_migraine_people <- 140
migraine_people <- tibble(
  participant_id = seq_len(n_migraine_people),
  medication = sample_cat(c("continuing", "reduced", "none"), n_migraine_people, c(0.45, 0.30, 0.25)),
  baseline_risk = rnorm(n_migraine_people, 0, 0.9)
)

migraine_headache_diary <- migraine_people |>
  rowwise() |>
  do({
    days <- sort(sample(-20:90, sample(18:80, 1)))
    tibble(participant_id = .$participant_id, day = days, medication = .$medication, baseline_risk = .$baseline_risk)
  }) |>
  ungroup() |>
  mutate(
    medication_effect = case_when(
      medication == "continuing" ~ 0.55,
      medication == "reduced" ~ 0.25,
      TRUE ~ -0.25
    ),
    time_effect = -0.006 * pmax(day, 0),
    headache_probability = logit(-0.15 + baseline_risk + medication_effect + time_effect),
    headache_reported = runif(n()) < headache_probability,
    headache = if_else(headache_reported, "yes", "no")
  ) |>
  select(participant_id, day, medication, headache, headache_reported) |>
  arrange(participant_id, day)

write_sim_csv(migraine_headache_diary, "toothache_diary.csv")
