metric_lookup <- tibble(
  metric_key = c("co_8hr_ppm", "lead_3mo_ug_m3", "no2_annual_mean_ppb", "no2_1hr_ppb", "ozone_8hr_ppb", "pm10_24hr_ug_m3", "pm25_annual_mean_ug_m3", "pm25_24hr_ug_m3", "so2_1hr_ppb", "so2_annual_mean_ppb"),
  pollutant = c("Hum", "Echo", "Traffic rumble", "Traffic spike", "Siren", "Construction dust", "Fine dust", "Fine dust spike", "Generator", "Generator annual"),
  statistic = c("8-hour", "3-month", "annual mean", "1-hour", "8-hour", "24-hour", "weighted annual mean", "24-hour", "1-hour", "weighted annual mean"),
  unit = c("index", "index", "index", "index", "index", "index", "index", "index", "index", "index"),
  naaqs_standard = c(9, 0.15, 53, 100, 70, 150, 9, 35, 75, 10),
  typical_fraction = c(0.35, 0.25, 0.45, 0.40, 0.65, 0.35, 0.75, 0.60, 0.25, 0.45)
)

n_monitor_counties <- 320
monitor_counties <- tibble(
  year = 2025,
  state = sample(state.abb, n_monitor_counties, replace = TRUE),
  county_index = seq_len(n_monitor_counties),
  population_2020 = round(rlnorm(n_monitor_counties, log(95000), 0.9))
) |>
  mutate(
    state_name = state.name[match(state, state.abb)],
    county_fips = sprintf("%02d%03d", match(state, state.abb), county_index %% 999),
    county_name = paste("Monitoring County", str_pad(county_index, 3, pad = "0"))
  )

epa_air_quality_county_2025_long <- tidyr::crossing(monitor_counties, metric_lookup) |>
  mutate(
    status = sample_cat(c("reported", "no_data", "insufficient_data"), n(), c(0.62, 0.31, 0.07)),
    value = if_else(status == "reported", naaqs_standard * pmax(0.02, rlnorm(n(), log(typical_fraction), 0.45)), NA_real_),
    value_as_fraction_of_standard = value / naaqs_standard,
    exceeds_standard = case_when(status != "reported" ~ NA, value > naaqs_standard ~ TRUE, TRUE ~ FALSE),
    value = round(value, 3),
    value_as_fraction_of_standard = round(value_as_fraction_of_standard, 3)
  ) |>
  select(year, state, state_name, county_fips, county_name, population_2020, pollutant, statistic, unit, metric_key, value, status, naaqs_standard, value_as_fraction_of_standard, exceeds_standard) |>
  arrange(state, county_name, pollutant, statistic)

epa_values <- epa_air_quality_county_2025_long |>
  select(year, state, state_name, county_fips, county_name, population_2020, metric_key, value) |>
  pivot_wider(names_from = metric_key, values_from = value)

