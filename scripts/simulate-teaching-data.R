  diastolic_2 = round(di_base + rnorm(nrow(bp_eligible), 0, 3)),
  systolic_3 = round(bp_base + rnorm(nrow(bp_eligible), -1, 4)),
  diastolic_3 = round(di_base + rnorm(nrow(bp_eligible), -1, 3)),
